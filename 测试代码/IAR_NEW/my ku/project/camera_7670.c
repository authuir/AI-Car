//SCCB起始信号
//当时钟为高的时候,数据线的高到低,为SCCB起始信号
//在激活状态下,SDA和SCL均为低电平
void SCCB_7670_Start(void)
{
    OV_SDA_Set;     //数据线高电平	
    SYSDelay_us(100);  
    OV_SCL_Set;	    //在时钟线高的时候数据线由高至低
    SYSDelay_us(100);  
    OV_SDA_Clear;
    SYSDelay_us(100);	 
    OV_SCL_Clear;	    //数据线恢复低电平，单操作函数必要	 
    SYSDelay_us(100);	 
}

//SCCB停止信号
//当时钟为高的时候,数据线的低到高,为SCCB停止信号
//空闲状况下,SDA,SCL均为高电平
void SCCB_7670_Stop(void)
 {
     OV_SDA_Clear;
     SYSDelay_us(100);
     OV_SCL_Set;
     SYSDelay_us(100);
     OV_SDA_Set; 
     SYSDelay_us(100);
 }



//产生NA信号
void SCCB_No_Ack(void)
{
	//SYSDelay_us(50);
	 OV_SDA_Set;	
     SYSDelay_us(100);
	OV_SCL_Set;	
	SYSDelay_us(100);
	OV_SCL_Clear;
	SYSDelay_us(100);
	OV_SDA_Clear;
	SYSDelay_us(100);
}


//SCCB,写入一个字节
//返回值:0,成功;1,失败. 
uint8 SCCB_WR_Byte(uint8 dat)
{
	uint8 j,res;	 
	for(j=0;j<8;j++) //循环8次发送数据
	{
		if(dat&0x80)OV_SDA_Set;	
		else OV_SDA_Clear;
		dat<<=1;
		SYSDelay_us(100);
		OV_SCL_Set;	
		SYSDelay_us(100);
		OV_SCL_Clear;		   
	}			 
	GPIO_INIT(OV7670_SCCB_SDA_PORT,OV7670_SCCB_SDA_PIN,0);		//设置SDA为输入 
	SYSDelay_us(100);
	OV_SCL_Set;			//接收第九位,以判断是否发送成功
	SYSDelay_us(100);
	if(GPIO_GET(OV7670_SCCB_SDA_PORT,OV7670_SCCB_SDA_PIN))res=1;  //SDA=1发送失败，返回1
	else res=0;         //SDA=0发送成功，返回0
	OV_SCL_Clear;
    SYSDelay_us(100);
	GPIO_INIT(OV7670_SCCB_SDA_PORT,OV7670_SCCB_SDA_PIN,1);		//设置SDA为输出    
	return res;  
}	

//SCCB 读取一个字节
//在SCL的上升沿,数据锁存
//返回值:读到的数据
uint8 SCCB_RD_Byte(void)
{
	uint8 temp=0,j;    
	GPIO_INIT(OV7670_SCCB_SDA_PORT,OV7670_SCCB_SDA_PIN,0);		//设置SDA为输入  
	for(j=8;j>0;j--) 	//循环8次接收数据
	{		     	  
		SYSDelay_us(100);
		OV_SCL_Set;
		temp=temp<<1;
		if(GPIO_GET(OV7670_SCCB_SDA_PORT,OV7670_SCCB_SDA_PIN))  temp++;   
		SYSDelay_us(100);
		OV_SCL_Clear;
	}	
	GPIO_INIT(OV7670_SCCB_SDA_PORT,OV7670_SCCB_SDA_PIN,1);		//设置SDA为输出    
	return temp;
} 		
//写寄存器
//返回值:0,成功;1,失败.
#define SCCB_write_reg  SCCB_WR_Reg
uint8 SCCB_WR_Reg(uint8 reg,uint8 data)
{
	uint8 res=0;
	SCCB_7670_Start(); 					//启动SCCB传输
	if(SCCB_WR_Byte(SCCB_ID))res=1;	//写器件ID	  
	SYSDelay_us(100);
  	if(SCCB_WR_Byte(reg))res=1;		//写寄存器地址	  
	SYSDelay_us(100);
  	if(SCCB_WR_Byte(data))res=1; 	//写数据	 
  	SCCB_7670_Stop();	  
  	return	res;
}		  					    
//读寄存器
//返回值:读到的寄存器值
uint8 SCCB_RD_Reg(uint8 reg)
{
	uint8 val=0;
	SCCB_7670_Start(); 				//启动SCCB传输
	SCCB_WR_Byte(SCCB_ID);		//写器件ID	  
	SYSDelay_us(100);	 
  	SCCB_WR_Byte(reg);			//写寄存器地址	  
	SYSDelay_us(100);	  
	//SCCB_7670_Stop();   
	//SYSDelay_us(100);	   
	//设置寄存器地址后，才是读
	SCCB_7670_Start();
	SCCB_WR_Byte(SCCB_ID|0X01);	//发送读命令	  
	SYSDelay_us(100);
  	val=SCCB_RD_Byte();		 	//读取数据
  	SCCB_No_Ack();
  	SCCB_7670_Stop();
  	return val;
}

